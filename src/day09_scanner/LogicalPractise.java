package day09_scanner;

public class LogicalPractise {
    public static void main(String[] args) {
        int n = 20;

        System.out.println(n>5);
        System.out.println(n<10);
        System.out.println(n>5 && n< 10);//true && false
        System.out.println(n>50|| false);
        System.out.println();


    }
}
