public class Practoce3Loop {
    public static void main(String[] args) {
        char l = 'z';
        while(l>='a'){
            System.out.println( " "+ l);
            l--;
        }

        char D ='Z';
        while (D>='A'){
            System.out.println(" " + D);
            D--;
        }

        int n=0;


        while(n <= 500){
            System.out.println(n);
            n++;
        }

    }
}
