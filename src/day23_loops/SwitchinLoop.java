package day23_loops;

public class SwitchinLoop {
    public static void main(String[] args) {

        for(int i = 0; i <=5; i++){

            switch (i){

                case 1:
                    System.out.println("Zero");
                    break;
                case 2:
                    System.out.println("One");
                    break;
                case 3:
                    System.out.println("Two");
                    break;
                case 4:
                    System.out.println("Three");
                    break;
                case 5:
                    System.out.println("Five");
                    break;
            }

        }
    }
}
